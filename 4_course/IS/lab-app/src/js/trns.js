Gost.prototype.translyator = function useTranslyator(data) {
    /*Функция принимает строку и возвращает его транслит*/

    let trans = dict_trans
    let split = "|"
    let dataSplit = data.split("")
    if (data.split(split).length - 1 > 1) {
        for (i of Object.keys(dict_untrans)) {
            if (data.includes(i)) {
                trans = dict_untrans
                dataSplit = data.split(split)
                split = ""
                break
            }
        }
    }
    let result = ""
    for (let i = 0; i < data.length; i++) {
        // console.log(`dataSplit[i] = ${dataSplit[i]}`);
        if (Object.keys(trans).includes(dataSplit[i])) {
            result += trans[dataSplit[i]]
            result += split
        }
    }
    return result
}